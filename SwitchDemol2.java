class SwitchDemol2 
{
	public static void main(String[] args) 
	{
		int a=5;
		switch (a)
		{
		case 1:
			System.out.println("aa");
		default:
			System.out.println("bye");
		//break;
		
		case 2:
			System.out.println("bb");
		case 3:
			System.out.println("cc");
		case 4:
			System.out.println("dd");
		
		}
		System.out.println("Hello World!");
	}
}
